/**
 * Escapes user-supplied strings for safe insertion into HTML email bodies.
 * Prevents XSS when user input is interpolated directly into HTML strings.
 */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#x27;');
}
